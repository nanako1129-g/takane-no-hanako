export const COMPANION_IDLE_SILENCE_MS = 30_000;

export function companionIdleCueForVenue(scene: "line" | "cafe" | "bar"): string {
  switch (scene) {
    case "cafe":
      return "【状況】喫茶店でふたりきり。ユーザーは30秒ほど話していない。恋人として気遣って、短く自然なひとことをかけてあげてほしい。「どうしたの？」や「ねぇ」「今ね、ちょっと君のこと考えてた」など、過剰にならない程度に。";
    case "bar":
      return "【状況】バー／飲みの席。ユーザーは30秒ほど話していない。恋人として、ほんの少し甘めに短いひとことをかけて。例：「大丈夫？」「何かあった？」「…君といるとなんかほっとする」など。長くしない。";
    default:
      return "【状況】LINE であなたの直前の一言のあと30秒ほどユーザーが無言。このあとメッセージを送れる。恋人同士として、短く気遣うひとことを。「どうしたの？」「今ね、ちょっと君のこと考えてた」「大丈夫？」など、文脈に合わせて1〜2文。";
  }
}
